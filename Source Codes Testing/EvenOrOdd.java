public class EvenOrOdd {
	public static void main(String args[]) {
		int num = 1;
		if(num%2==0){
			System.out.println("The number "+num+" is even");
		}
		else if(num == 1){
			System.out.println("The number "+num+" is neither even nor odd");
		}
		else{
			System.out.println("The number "+num+" is odd");
		}
		
	}

}
