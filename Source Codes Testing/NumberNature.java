public class NumberNature {
	public static void main(String args[]) {
		int num = -87;
		if(num>0){
			System.out.println("The number "+num+" is positive");
		}
		else if(num == 0){
			System.out.println("The number "+num+" is zero");
		}
		else{
			System.out.println("The number "+num+" is negative");
		}
		
	}

}
